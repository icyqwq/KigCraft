export function translateLiteralString(value: string, language: string) {
  const locale = getLiteralLocale(language);
  if (!locale) return value;

  const exact = translations[locale][value];
  if (exact) return exact;

  const trimmed = value.trim();
  const trimmedExact = translations[locale][trimmed];
  if (trimmedExact) return value.replace(trimmed, trimmedExact);

  for (const pattern of patterns) {
    const match = trimmed.match(pattern.match);
    if (match) return value.replace(trimmed, pattern.replace(match, locale));
  }

  return value;
}

export function translateLiteralNode(node: ReactNode, language: string): ReactNode {
  if (typeof node === "string") return translateLiteralString(node, language);
  if (Array.isArray(node)) return Children.map(node, (child) => translateLiteralNode(child, language));
  if (!isValidElement(node)) return node;
  return node;
}

function getLiteralLocale(language: string | undefined): LiteralLocale | null {
  if (!language) return null;
  const normalized = language.toLowerCase();
  if (normalized.startsWith("en")) return "en";
  if (normalized.startsWith("ja")) return "ja";
  return null;
}

function translateExact(value: string, locale: LiteralLocale) {
  return translations[locale][value.trim()] ?? value;
}
